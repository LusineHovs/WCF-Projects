        private void Button_Click(object sender, RoutedEventArgs e)
        {
            var proxy = new BetServiceClient();
            var persons = await proxy.GetPersonsAsync();
            foreach (var item in persons)
            {
                Debug.WriteLine($"{item.Name}: {item.Age}");
            }
        }
